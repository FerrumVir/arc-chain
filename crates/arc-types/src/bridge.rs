/// Configuration for a specific chain-pair bridge route.
#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct BridgeConfig {
    /// Source chain for this route.
    pub source_chain: ChainId,
    /// Destination chain for this route.
    pub dest_chain: ChainId,
    /// Bridge contract address on the source chain.
    pub bridge_contract: EvmAddress,
    /// Minimum source chain confirmations before proof generation.
    pub min_confirmations: u32,
    /// Maximum transfer amount (in smallest unit).
    pub max_transfer_amount: u128,
    /// Minimum transfer amount (in smallest unit).
    pub min_transfer_amount: u128,
    /// Fee in basis points (100 = 1%).
    pub fee_bps: u16,
    /// Blocks before an uncompleted transfer expires.
    pub timeout_blocks: u64,
    /// Whether this bridge route is currently accepting transfers.
    pub is_active: bool,
}

impl BridgeConfig {
    /// Default configuration for Ethereum -> ARC Mainnet bridge.
    pub fn default_eth_to_arc() -> Self {
        Self {
            source_chain: ChainId::Ethereum,
            dest_chain: ChainId::ArcMainnet,
            bridge_contract: [0u8; 20], // Placeholder — set after deployment
            min_confirmations: 12,
            max_transfer_amount: 1_000_000_000_000_000_000_000, // 1000 ETH in wei
            min_transfer_amount: 10_000_000_000_000_000,        // 0.01 ETH in wei
            fee_bps: 30,                                        // 0.30%
            timeout_blocks: 7200,                                // ~24h at 12s blocks
            is_active: true,
        }
    }

    /// Default configuration for ARC Mainnet -> Ethereum bridge.
    pub fn default_arc_to_eth() -> Self {
        Self {
            source_chain: ChainId::ArcMainnet,
            dest_chain: ChainId::Ethereum,
            bridge_contract: [0u8; 20], // Placeholder — set after deployment
            min_confirmations: 1,
            max_transfer_amount: 1_000_000_000_000_000_000_000,
            min_transfer_amount: 10_000_000_000_000_000,
            fee_bps: 30,
            timeout_blocks: 14400,
            is_active: true,
        }
    }

    /// Check whether a transfer amount falls within the allowed range.
    pub fn is_amount_valid(&self, amount: u128) -> bool {
        amount >= self.min_transfer_amount && amount <= self.max_transfer_amount
    }
}

