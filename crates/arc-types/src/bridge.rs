    // 6. Valid Merkle proof verification
    #[test]
    fn test_bridge_proof_verification() {
        // Build a small Merkle tree manually: 4 leaves
        let leaves: Vec<[u8; 32]> = (0u8..4)
            .map(|i| *blake3::hash(&[i]).as_bytes())
            .collect();

        // Level 0: [L0, L1, L2, L3]
        // Level 1: [H(L0,L1), H(L2,L3)]
        // Level 2: [H(H(L0,L1), H(L2,L3))]  <- root

        let h01 = {
            let mut h = blake3::Hasher::new();
            h.update(&leaves[0]);
            h.update(&leaves[1]);
            *h.finalize().as_bytes()
        };
        let h23 = {
            let mut h = blake3::Hasher::new();
            h.update(&leaves[2]);
            h.update(&leaves[3]);
            *h.finalize().as_bytes()
        };
        let root = {
            let mut h = blake3::Hasher::new();
            h.update(&h01);
            h.update(&h23);
            *h.finalize().as_bytes()
        };

        // Prove leaf[0]: siblings = [L1, H(L2,L3)]
        let proof = BridgeProof {
            transfer_id: [0u8; 32],
            source_block_hash: [0u8; 32],
            source_block_height: 100,
            merkle_siblings: vec![leaves[1], h23],
            leaf_index: 0,
            leaf_hash: leaves[0],
        };

        assert!(proof.verify(&root));
    }

