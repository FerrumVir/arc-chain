/// 20-byte EVM-compatible address used in bridge contexts.
/// Both ARC Chain and external EVM chains use this format for cross-chain transfers.
pub type EvmAddress = [u8; 20];

/// Zero EVM address — indicates native token (ETH, ARC, etc.).
pub const ZERO_EVM_ADDRESS: EvmAddress = [0u8; 20];

// ─── Chain Identifiers ──────────────────────────────────────────────────────

/// Supported bridge chains.
#[derive(Debug, Clone, Copy, PartialEq, Eq, Hash, Serialize, Deserialize)]
pub enum ChainId {
    ArcMainnet,
    Ethereum,
    Base,
    Arbitrum,
    Optimism,
    Polygon,
    Bsc,
    Avalanche,
    /// Arbitrary EVM chain by numeric chain ID.
    Custom(u64),
}

impl ChainId {
    /// Return the EVM-standard numeric chain ID.
    pub fn evm_chain_id(&self) -> u64 {
        match self {
            ChainId::ArcMainnet => 0xA4C, // 2636 — ARC custom chain ID
            ChainId::Ethereum => 1,
            ChainId::Base => 8453,
            ChainId::Arbitrum => 42161,
            ChainId::Optimism => 10,
            ChainId::Polygon => 137,
            ChainId::Bsc => 56,
            ChainId::Avalanche => 43114,
            ChainId::Custom(id) => *id,
        }
    }

    /// Returns true if the chain is EVM-compatible (all currently supported chains are).
    pub fn is_evm_compatible(&self) -> bool {
        true
    }

    /// Human-readable chain name.
    pub fn name(&self) -> &str {
        match self {
            ChainId::ArcMainnet => "ARC Mainnet",
            ChainId::Ethereum => "Ethereum",
            ChainId::Base => "Base",
            ChainId::Arbitrum => "Arbitrum One",
            ChainId::Optimism => "Optimism",
            ChainId::Polygon => "Polygon",
            ChainId::Bsc => "BNB Smart Chain",
            ChainId::Avalanche => "Avalanche C-Chain",
            ChainId::Custom(id) => {
                // Can't return a dynamic string from &str, so use a generic label
                // Callers needing the ID should use evm_chain_id() directly.
                let _ = id;
                "Custom EVM Chain"
            }
        }
    }
}

// ─── Bridge Transfer ────────────────────────────────────────────────────────

/// Bridge transfer status lifecycle.
#[derive(Debug, Clone, Copy, PartialEq, Eq, Serialize, Deserialize)]
pub enum BridgeStatus {
    /// Transfer submitted, awaiting lock on source chain.
    Pending,
    /// Assets locked on source chain.
    SourceLocked,
    /// Merkle proof being generated by relayer.
    Proving,
    /// Wrapped assets minted on destination chain.
    DestMinted,
    /// Confirmed on both chains — terminal success state.
    Completed,
    /// Transaction reverted — terminal failure state.
    Failed,
    /// Timeout reached without completion — terminal failure state.
    Expired,
}

/// Cross-chain bridge transfer request.
///
/// Represents a lock-and-mint bridge operation: assets are locked on the source
/// chain and equivalent wrapped assets are minted on the destination chain.
#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct BridgeTransfer {
    /// Unique transfer ID (BLAKE3 hash of all content fields).
    pub transfer_id: [u8; 32],
    /// Chain where assets are locked.
    pub source_chain: ChainId,
    /// Chain where wrapped assets are minted.
    pub dest_chain: ChainId,
    /// Sender address on source chain.
    pub source_address: EvmAddress,
    /// Recipient address on destination chain.
    pub dest_address: EvmAddress,
    /// Token contract on source chain (zero address = native token).
    pub token_address: EvmAddress,
    /// Amount in smallest unit (wei / lamports / etc.).
    pub amount: u128,
    /// Sequence number per sender (replay protection).
    pub nonce: u64,
    /// Unix timestamp of transfer creation.
    pub timestamp: u64,
    /// Current status in the bridge lifecycle.
    pub status: BridgeStatus,
}

impl BridgeTransfer {
    /// Create a new pending bridge transfer and compute its transfer ID.
    pub fn new(
        source: ChainId,
        dest: ChainId,
        from: EvmAddress,
        to: EvmAddress,
        token: EvmAddress,
        amount: u128,
        nonce: u64,
    ) -> Self {
        let timestamp = std::time::SystemTime::now()
            .duration_since(std::time::UNIX_EPOCH)
            .unwrap_or_default()
            .as_secs();

        let mut transfer = Self {
            transfer_id: [0u8; 32],
            source_chain: source,
            dest_chain: dest,
            source_address: from,
            dest_address: to,
            token_address: token,
            amount,
            nonce,
            timestamp,
            status: BridgeStatus::Pending,
        };
        transfer.transfer_id = transfer.compute_transfer_id();
        transfer
    }

    /// Compute the deterministic transfer ID from all content fields.
    ///
    /// Covers: source_chain || dest_chain || source_address || dest_address
    ///         || token_address || amount || nonce || timestamp
    pub fn compute_transfer_id(&self) -> [u8; 32] {
        let mut hasher = blake3::Hasher::new_derive_key("ARC-bridge-transfer-v1");
        hasher.update(&self.source_chain.evm_chain_id().to_le_bytes());
        hasher.update(&self.dest_chain.evm_chain_id().to_le_bytes());
        hasher.update(&self.source_address);
        hasher.update(&self.dest_address);
        hasher.update(&self.token_address);
        hasher.update(&self.amount.to_le_bytes());
        hasher.update(&self.nonce.to_le_bytes());
        hasher.update(&self.timestamp.to_le_bytes());
        *hasher.finalize().as_bytes()
    }

    /// Returns true if this transfer uses the native token (zero address).
    pub fn is_native_token(&self) -> bool {
        self.token_address == ZERO_EVM_ADDRESS
    }

    /// Returns true if the transfer has reached a terminal state.
    pub fn is_finalized(&self) -> bool {
        matches!(
            self.status,
            BridgeStatus::Completed | BridgeStatus::Failed | BridgeStatus::Expired
        )
    }

    /// Calculate the estimated bridge fee for this transfer given a config.
    ///
    /// Fee = amount * fee_bps / 10_000
    pub fn estimated_fee(&self, config: &BridgeConfig) -> u128 {
        self.amount
            .checked_mul(config.fee_bps as u128)
            .unwrap_or(u128::MAX)
            / 10_000
    }
}

// ─── Bridge Proof ───────────────────────────────────────────────────────────

/// Merkle inclusion proof for cross-chain bridge verification.
///
/// The relayer generates this proof from the source chain's state to prove
/// that a lock transaction was included in a finalized block.
#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct BridgeProof {
    /// Transfer ID this proof corresponds to.
    pub transfer_id: [u8; 32],
    /// Block hash on source chain containing the lock transaction.
    pub source_block_hash: [u8; 32],
    /// Block height on source chain.
    pub source_block_height: u64,
    /// Merkle sibling hashes from leaf to root.
    pub merkle_siblings: Vec<[u8; 32]>,
    /// Index of the leaf in the Merkle tree.
    pub leaf_index: u64,
    /// Hash of the leaf (the transfer commitment).
    pub leaf_hash: [u8; 32],
}

impl BridgeProof {
    /// Verify this Merkle proof against an expected root hash.
    ///
    /// Walks from the leaf up through the sibling path, hashing pairs,
    /// and checks whether the computed root matches the expected root.
    pub fn verify(&self, expected_root: &[u8; 32]) -> bool {
        let mut current = self.leaf_hash;
        let mut index = self.leaf_index;

        for sibling in &self.merkle_siblings {
            let mut hasher = blake3::Hasher::new();
            if index % 2 == 0 {
                // Current is the left child
                hasher.update(&current);
                hasher.update(sibling);
            } else {
                // Current is the right child
                hasher.update(sibling);
                hasher.update(&current);
            }
            current = *hasher.finalize().as_bytes();
            index /= 2;
        }

        current == *expected_root
    }
}

