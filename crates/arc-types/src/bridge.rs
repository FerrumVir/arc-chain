/// Message submitted by a bridge relayer to the destination chain.
///
/// Contains the original transfer, the Merkle inclusion proof, and the
/// relayer's signature authorizing the mint operation.
#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct BridgeRelayMessage {
    /// The original bridge transfer being relayed.
    pub transfer: BridgeTransfer,
    /// Merkle proof of lock inclusion on the source chain.
    pub proof: BridgeProof,
    /// Address of the relayer submitting this message.
    pub relayer_address: EvmAddress,
    /// Relayer's signature over (transfer_id || proof commitment).
    pub relayer_signature: Vec<u8>,
}

