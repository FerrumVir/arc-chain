// ─── Bridge Stats ───────────────────────────────────────────────────────────

/// Aggregate statistics for bridge activity.
#[derive(Debug, Clone, Default)]
pub struct BridgeStats {
    /// Total number of transfers ever submitted.
    pub total_transfers: u64,
    /// Cumulative volume (in smallest unit of the bridge's base token).
    pub total_volume: u128,
    /// Number of currently non-finalized transfers.
    pub pending_transfers: u64,
    /// Number of successfully completed transfers.
    pub completed_transfers: u64,
    /// Number of failed or expired transfers.
    pub failed_transfers: u64,
    /// Number of unique sender addresses.
    pub unique_users: u64,
}

impl BridgeStats {
    /// Record a bridge transfer, updating all counters.
    pub fn record_transfer(&mut self, transfer: &BridgeTransfer) {
        self.total_transfers += 1;
        self.total_volume = self.total_volume.saturating_add(transfer.amount);

        match transfer.status {
            BridgeStatus::Completed => {
                self.completed_transfers += 1;
            }
            BridgeStatus::Failed | BridgeStatus::Expired => {
                self.failed_transfers += 1;
            }
            _ => {
                self.pending_transfers += 1;
            }
        }
    }

    /// Percentage of completed transfers out of all finalized transfers.
    /// Returns 0.0 if no transfers have been finalized.
    pub fn success_rate(&self) -> f64 {
        let finalized = self.completed_transfers + self.failed_transfers;
        if finalized == 0 {
            return 0.0;
        }
        self.completed_transfers as f64 / finalized as f64
    }
}

